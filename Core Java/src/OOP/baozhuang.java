package OOP;

public class baozhuang {

	public static void main(String[] args) {
		// 将int转换成string类型
		Integer integer = 10;
		String string = integer.toString();
		System.out.println(string);

		// 将string转换成int
		String str = "20";
		int a = Integer.parseInt(str);
		System.out.println(a);

	}

}
